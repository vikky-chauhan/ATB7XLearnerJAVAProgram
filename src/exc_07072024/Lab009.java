package exc_07072024;

public class Lab009 {

    public static void main(String[] args) {

        // Compound Assignment Operator

        int a=10;
         // +=, *=, %=, /=,  //+, ->

        a +=10;  // that's mean a=a+1 ==== Answer is 20
        a -=10 ; // that's mean a=a-10  == 0
        a *=10 ; // a=a*10   == 100
        a /= 10 ; // a = a/10 == 1


    }
}
